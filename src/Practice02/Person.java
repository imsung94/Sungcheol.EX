package Practice02;

public class Person {
	
	String name;
	float height;
	float weight;
	
	public Person() {}

	public Person(String pname) {
		name = pname;
	}

	public Person(String pname, float pheight, float pweight) {
		super();
		this.name = pname;
		this.height = pheight;
		this.weight = pweight;
	}
	

}
